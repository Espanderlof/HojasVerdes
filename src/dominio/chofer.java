
package dominio;

public class chofer {
    private int rut_chofer;
    private String nom_chofer;
    private int telefono;
    private String direccion;

    public chofer() {
    }

    public int getRut_chofer() {
        return rut_chofer;
    }

    public void setRut_chofer(int rut_chofer) {
        this.rut_chofer = rut_chofer;
    }

    public String getNom_chofer() {
        return nom_chofer;
    }

    public void setNom_chofer(String nom_chofer) {
        this.nom_chofer = nom_chofer;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }
    
    
    
}
